    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // First, we read text from the pdf file
            PDF objPdf = new PDF();
            String pdfText = objPdf.ReadText(System.getProperty("user.dir") + "\\data\\sample_invoice.pdf");
            TextProcessor objTextProc = new TextProcessor();
            if (!txtCustomerNameKey.getText().trim().equals("")) {
                lblCustomerName.setText(objTextProc.ExtractFields(pdfText, txtCustomerNameKey.getText().trim()));
            } else {
                lblCustomerName.setText("-");
            }
            if (!txtInvoiceAmountKey.getText().trim().equals("")) {
                lblInvoiceAmount.setText(objTextProc.ExtractFields(pdfText, txtInvoiceAmountKey.getText().trim()));
            } else {
                lblInvoiceAmount.setText("-");
            }
        } catch (IOException ex) {
            Logger.getLogger(index.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

